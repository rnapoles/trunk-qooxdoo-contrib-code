/**
 * This is the main application class of your custom application "themed_iframe"
 */
qx.Class.define("custom.Application",
{
  extend : qx.application.Standalone,



  /*
  *****************************************************************************
     MEMBERS
  *****************************************************************************
  */

  members :
  {
    /**
     * This method contains the initial application code and gets called 
     * during startup of the application
     */
    main : function()
    {
      // Call super class
      this.base(arguments);

      // Enable logging in debug variant
      if (qx.core.Variant.isSet("qx.debug", "on"))
      {
        // support native logging capabilities, e.g. Firebug for Firefox
        qx.log.appender.Native;
        // support additional cross-browser console. Press F7 to toggle visibility
        qx.log.appender.Console;
      }

      /*
      -------------------------------------------------------------------------
        Below is your actual application code...
      -------------------------------------------------------------------------
      */

      var iframe = new qx.ui.embed.ThemedIframe(qx.util.ResourceManager.getInstance().toUri("custom/inner.html")).set({
        width: 300,
        height: 50,
        minHeight: 0
      });
      this.getRoot().add(iframe, {left: 10, top: 10});
    }
  }
});
