/**
 * A simple button
 */
qx.Class.define("skeleton.SimpleButton",
{
  extend : qx.ui.form.Button,


  /*
  *****************************************************************************
     CONSTRUCTOR
  *****************************************************************************
  */

  construct : function(text)
  {
    this.base(arguments, text, qx.core.Setting.get("skeleton.resourceUri") + "/image/test.png");
  },
  
  
  /*
  *****************************************************************************
     SETTINGS
  *****************************************************************************
  */

  settings : {
    "skeleton.resourceUri" : "./resource"
  }
  
});